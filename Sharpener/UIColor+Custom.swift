import Foundation
import UIKit

extension UIColor {
    class func spOutlineColor() -> UIColor {
        return UIColor(red:102/255, green:109/255, blue:129/255, alpha:1)
    }
    
    class func spGreenColor() -> UIColor {
        return UIColor(red:212/255, green:238/255, blue:231/255, alpha:1)
    }
    
    class func spLightBorderColor() -> UIColor {
        return UIColor(red:196/255, green:196/255, blue:196/255, alpha:1)
    }
    
    class func spYellowColor() -> UIColor {
        return UIColor(red:255/255, green:251/255, blue:181/255, alpha:1)
    }
    
    class func spLineColor() -> UIColor {
        return UIColor(red:209/255, green:117/255, blue:120/255, alpha:1)
    }
    
    class func spShapeColor() -> UIColor {
        return UIColor(red:107/255, green:181/255, blue:161/255, alpha:1)
    }
}

